@pytest.mark.usefixtures("test_client", "mo_engagement")
async def test_legacy_template_does_many_lookups(
    trigger_ldap_person: Callable[[], Awaitable[None]],
    ldap_search_counter: Callable[[], int],
) -> None:
    await trigger_ldap_person()
    # 1 initial event fetch + 1 uuid pre-lookup (used to find the existing MO
    # object) + 1 search per template field (7 fields use the helper) = 9.
    assert ldap_search_counter() == 9


