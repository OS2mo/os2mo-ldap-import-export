"""Prove that an engagement-style ldap_to_mo_any mapping using
`find_mo_employee_uuid_by_guid_and_cpr` triggers exactly one LDAP search,
whereas the equivalent mapping using `find_mo_employee_uuid(ldap.dn)` issues
many redundant searches.
"""

import json
from collections.abc import Awaitable
from collections.abc import Callable
from typing import cast
from uuid import UUID

import pytest
from fastramqpi.context import Context
from httpx import AsyncClient

from mo_ldap_import_export.autogenerated_graphql_client import EngagementCreateInput
from mo_ldap_import_export.autogenerated_graphql_client import GraphQLClient
from mo_ldap_import_export.autogenerated_graphql_client import RAValidityInput
from mo_ldap_import_export.dataloaders import DataLoader


@pytest.fixture
def dataloader(test_client: AsyncClient, context: Context) -> DataLoader:
    return cast(DataLoader, context["user_context"]["dataloader"])


@pytest.fixture
def ldap_search_counter(
    monkeypatch: pytest.MonkeyPatch, dataloader: DataLoader
) -> Callable[[], int]:
    """Count every LDAP search issued through the running app's connection."""
    state = {"count": 0}
    connection = dataloader.ldapapi.ldap_connection.connection
    original = connection.search

    def counted(*args, **kwargs):
        state["count"] += 1
        return original(*args, **kwargs)

    monkeypatch.setattr(connection, "search", counted)
    return lambda: state["count"]


@pytest.fixture
async def mo_engagement(
    graphql_client: GraphQLClient,
    mo_person: UUID,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
) -> None:
    await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key="engagement",
            person=mo_person,
            org_unit=mo_org_unit,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            extension_1="create",
            validity=RAValidityInput(from_="2001-02-03T04:05:06Z"),
        )
    )


