async def test_refresh_chain_graphql(
    amqp_emit_events: Callable[[], Awaitable[None]],
    get_ldap_person: Callable[[UUID], Awaitable[dict[str, Any] | None]],
    graphql_client: GraphQLClient,
    mo_person: UUID,
    fastramqpi: FastRAMQPI,
    app: FastAPI,
) -> None:
    borgerpc = await graphql_client.itsystem_create(
        input=ITSystemCreateInput(
            user_key="borgerpc",
            name="BorgerPC",
            validity={"from": "2001-02-03T04:05:06Z"},
        )
    )
    await graphql_client.ituser_create(
        input=ITUserCreateInput(
            user_key="mo_person_bpc",
            itsystem=borgerpc.uuid,
            person=mo_person,
            validity={"from": "2001-02-03T04:05:06Z"},
        )
    )

    # One shot emit events to ensure all fixture events have been fired
    await amqp_emit_events()

    # Check that no LDAP person exists for the mo_person
    ldap_person = await get_ldap_person(mo_person)
    assert ldap_person is None

    # Start the integration
    async with run_server(app):
        amqpsystem = fastramqpi.get_amqpsystem()
        # Triggering the itsystem should run the itsystem handler, which should trigger
        # the person handler, which should create our person
        await refresh(graphql_client, amqpsystem, "itsystem", {borgerpc.uuid})

        async for attempt in retrying():
            with attempt:
                ldap_person = await get_ldap_person(mo_person)
                assert ldap_person is not None

    assert ldap_person == {
        "raw_dn": ANY,
        "dn": "cn=2108613133,ou=os2mo,o=magenta,dc=magenta,dc=dk",
        "raw_attributes": ANY,
        "attributes": {
            "carLicense": [str(mo_person)],
            "displayName": " ",
            "employeeNumber": "2108613133",
            "givenName": ["Aage"],
            "sn": ["Bach Klarskov"],
            "uid": ["2108613133"],
            "objectClass": ["inetOrgPerson"],
            "cn": ["2108613133"],
        },
        "type": "searchResEntry",
    }
