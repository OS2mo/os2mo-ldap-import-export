@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
        "LISTEN_TO_CHANGES_IN_MO": "False",
    }
)
@pytest.mark.parametrize(
    "engagement_to,ituser_to",
    [
        # Active IT-user (no termination): engagement is not dangling.
        pytest.param(None, None, id="active_ituser"),
        # IT-user terminated, but engagement already ends earlier: not dangling.
        pytest.param(
            datetime(2019, 1, 1, tzinfo=MO_TZ),
            ITUSER_TERMINATION,
            id="engagement_ends_before_ituser",
        ),
    ],
)
async def test_no_dangling_relations(
    engagement_to: datetime | None,
    ituser_to: datetime | None,
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    aduuid_itsystem: UUID,
    mo_person: UUID,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
) -> None:
    """The endpoint returns nothing when no engagement outlives its IT-user."""
    engagement_uuid = await _create_engagement(
        graphql_client,
        "eng",
        mo_person,
        mo_org_unit,
        ansat,
        jurist,
        primary,
        to=engagement_to,
    )
    ituser_uuid = await _create_ituser(
        graphql_client,
        "ituser",
        aduuid_itsystem,
        mo_person,
        engagement=engagement_uuid,
    )
    if ituser_to is not None:
        await graphql_client.ituser_terminate(
            input=ITUserTerminateInput(uuid=ituser_uuid, to=ituser_to)
        )

    response = await test_client.post(
        ENDPOINT, params={"it_system": str(aduuid_itsystem)}
    )
    assert response.status_code == 200
    assert response.json() == {}
    assert await _engagement_to(graphql_client, engagement_uuid) == engagement_to


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
        "LISTEN_TO_CHANGES_IN_MO": "False",
    }
)
@pytest.mark.parametrize(
    "engagement_to",
    [
        pytest.param(None, id="open_ended_engagement"),
        pytest.param(
            datetime(2025, 1, 1, tzinfo=MO_TZ), id="engagement_ends_after_ituser"
        ),
    ],
)
async def test_terminate_dangling_engagement_and_address(
    engagement_to: datetime | None,
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    aduuid_itsystem: UUID,
    mo_person: UUID,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
    email_employee: UUID,
) -> None:
    """A terminated IT-user with a dangling engagement and dangling address
    should have both terminated at the IT-user's termination date.

    Unrelated engagements and addresses must remain untouched.
    """
    dangling_engagement = await _create_engagement(
        graphql_client,
        "dangling-eng",
        mo_person,
        mo_org_unit,
        ansat,
        jurist,
        primary,
        to=engagement_to,
    )
    untouched_engagement = await _create_engagement(
        graphql_client,
        "untouched-eng",
        mo_person,
        mo_org_unit,
        ansat,
        jurist,
        primary,
    )

    ituser_uuid = await _create_ituser(
        graphql_client,
        "terminated-ituser",
        aduuid_itsystem,
        mo_person,
        engagement=dangling_engagement,
    )

    dangling_address = await graphql_client.address_create(
        input=AddressCreateInput(
            user_key="dangling-addr",
            person=mo_person,
            address_type=email_employee,
            value="dangling@example.com",
            ituser=ituser_uuid,
            validity=RAValidityInput(from_=EPOCH),
        )
    )
    # An address without an ituser link must not be touched.
    untouched_address = await graphql_client.address_create(
        input=AddressCreateInput(
            user_key="untouched-addr",
            person=mo_person,
            address_type=email_employee,
            value="untouched@example.com",
            validity=RAValidityInput(from_=EPOCH),
        )
    )

    await graphql_client.ituser_terminate(
        input=ITUserTerminateInput(uuid=ituser_uuid, to=ITUSER_TERMINATION)
    )

    # Dry-run reports the dangling relations.
    dry_response = await test_client.post(
        ENDPOINT, params={"it_system": str(aduuid_itsystem)}
    )
    assert dry_response.status_code == 200
    body = dry_response.json()
    assert set(body.keys()) == {str(ituser_uuid)}
    entry = body[str(ituser_uuid)]
    assert entry["engagements"] == [str(dangling_engagement)]
    assert entry["addresses"] == [str(dangling_address.uuid)]
    assert entry["termination_date"].startswith("2020-06-15")

    # Dry-run must not mutate state.
    assert await _engagement_to(graphql_client, dangling_engagement) == engagement_to
    assert await _address_to(graphql_client, dangling_address.uuid) is None

    # Real run terminates the dangling relations.
    run_response = await test_client.post(
        ENDPOINT,
        params={"it_system": str(aduuid_itsystem), "dry_run": False},
    )
    assert run_response.status_code == 200
    assert run_response.json() == body

    assert (
        await _engagement_to(graphql_client, dangling_engagement) == ITUSER_TERMINATION
    )
    assert (
        await _address_to(graphql_client, dangling_address.uuid) == ITUSER_TERMINATION
    )
    # The unrelated engagement and address are unchanged.
    assert await _engagement_to(graphql_client, untouched_engagement) is None
    assert await _address_to(graphql_client, untouched_address.uuid) is None

    # A subsequent dry-run finds nothing left.
    after = await test_client.post(ENDPOINT, params={"it_system": str(aduuid_itsystem)})
    assert after.status_code == 200
    assert after.json() == {}


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
        "LISTEN_TO_CHANGES_IN_MO": "False",
    }
)
async def test_it_system_parameter_scopes_the_search(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    aduuid_itsystem: UUID,
    mo_person: UUID,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
) -> None:
    """The endpoint only considers IT-users in the supplied IT-system.

    A terminated IT-user under one IT-system must not be reported when the
    other IT-system is queried, and vice versa.
    """
    other_itsystem = (
        await graphql_client.itsystem_create(
            input=ITSystemCreateInput(
                user_key="Other",
                name="Other IT-system",
                validity=RAOpenValidityInput(from_="1970-01-01T00:00:00Z"),
            )
        )
    ).uuid

    engagement_uuid = await _create_engagement(
        graphql_client,
        "other-eng",
        mo_person,
        mo_org_unit,
        ansat,
        jurist,
        primary,
    )
    ituser_uuid = await _create_ituser(
        graphql_client,
        "other-ituser",
        other_itsystem,
        mo_person,
        engagement=engagement_uuid,
    )
    await graphql_client.ituser_terminate(
        input=ITUserTerminateInput(uuid=ituser_uuid, to=ITUSER_TERMINATION)
    )

    # Querying the LDAP IT-system: invisible.
    aduuid_response = await test_client.post(
        ENDPOINT, params={"it_system": str(aduuid_itsystem)}
    )
    assert aduuid_response.status_code == 200
    assert aduuid_response.json() == {}

    # Querying the actual owning IT-system: picked up.
    other_response = await test_client.post(
        ENDPOINT, params={"it_system": str(other_itsystem)}
    )
    assert other_response.status_code == 200
    body = other_response.json()
    assert set(body.keys()) == {str(ituser_uuid)}
    assert body[str(ituser_uuid)]["engagements"] == [str(engagement_uuid)]

    # Terminate, then verify it actually ran.
    run_response = await test_client.post(
        ENDPOINT,
        params={"it_system": str(other_itsystem), "dry_run": False},
    )
    assert run_response.status_code == 200
    assert run_response.json() == body
    assert await _engagement_to(graphql_client, engagement_uuid) == ITUSER_TERMINATION
