ENDPOINT = "/fixup/terminate_dangling_it_user_relations"
EPOCH = datetime(1980, 1, 1, tzinfo=MO_TZ)
ITUSER_TERMINATION = datetime(2020, 6, 15, tzinfo=MO_TZ)


@pytest.fixture
async def aduuid_itsystem(
    read_itsystem_by_user_key: Callable[[str], Awaitable[UUID]],
    aduuid: UUID,
) -> UUID:
    return await read_itsystem_by_user_key("ADUUID")


async def _engagement_to(graphql_client: GraphQLClient, uuid: UUID) -> datetime | None:
    result = await graphql_client.read_engagements(
        EngagementFilter(uuids=[uuid], from_date=None, to_date=None)
    )
    return last(one(result.objects).validities).validity.to


async def _address_to(graphql_client: GraphQLClient, uuid: UUID) -> datetime | None:
    result = await graphql_client._testing__address_read(
        filter=AddressFilter(uuids=[uuid], from_date=None, to_date=None)
    )
    return last(one(result.objects).validities).validity.to


async def _create_engagement(
    graphql_client: GraphQLClient,
    user_key: str,
    person: UUID,
    org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
    to: datetime | None = None,
) -> UUID:
    result = await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key=user_key,
            person=person,
            org_unit=org_unit,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            validity=RAValidityInput(from_=EPOCH, to=to),
        )
    )
    return result.uuid


async def _create_ituser(
    graphql_client: GraphQLClient,
    user_key: str,
    itsystem: UUID,
    person: UUID,
    engagement: UUID | None = None,
) -> UUID:
    result = await graphql_client.ituser_create(
        input=ITUserCreateInput(
            user_key=user_key,
            itsystem=itsystem,
            person=person,
            engagement=engagement,
            validity=RAValidityInput(from_=EPOCH),
        )
    )
    return result.uuid


