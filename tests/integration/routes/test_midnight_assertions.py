@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
        "LISTEN_TO_CHANGES_IN_MO": "False",
    }
)
@pytest.mark.parametrize(
    "at",
    [
        "2026-06-01T00:00:00Z",  # aware: midnight UTC == 02:00 Copenhagen
        "2026-06-01T02:00:00",  # naive: assumed Copenhagen, not midnight
    ],
)
@pytest.mark.usefixtures("ldap_person")
async def test_purge_addresses_at_not_midnight(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    mo_person: UUID,
    email_employee: UUID,
    at: str,
) -> None:
    # Two addresses of the same type so the cleanup loop has something to terminate.
    for value in ("a@example.org", "b@example.org"):
        await graphql_client.address_create(
            input=AddressCreateInput(
                user_key="test",
                person=mo_person,
                address_type=email_employee,
                value=value,
                validity=RAValidityInput(from_="1980-01-01T00:00:00"),
            )
        )

    response = await test_client.delete(
        "/danger/purge_addresses",
        params={
            "address_type_user_key": "EmailEmployee",
            "at": at,
            "dry_run": "False",
        },
    )
    assert response.status_code == 500
    assert "AssertionError: 'at' is not at midnight" in response.text
