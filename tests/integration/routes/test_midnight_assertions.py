"""Tests for midnight enforcement on endpoints that forward `at` to MO."""

import json
from collections.abc import Awaitable
from collections.abc import Callable
from uuid import UUID
from uuid import uuid4

import pytest
from httpx import AsyncClient

from mo_ldap_import_export.autogenerated_graphql_client import AddressCreateInput
from mo_ldap_import_export.autogenerated_graphql_client import GraphQLClient
from mo_ldap_import_export.autogenerated_graphql_client import RAValidityInput
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    ITSystemCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    ITUserCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    RAOpenValidityInput,
)


@pytest.fixture
async def account_name_itsystem(graphql_client: GraphQLClient) -> UUID:
    itsystem = await graphql_client.itsystem_create(
        input=ITSystemCreateInput(
            user_key="account_name",
            name="Account Name",
            validity=RAOpenValidityInput(from_="1970-01-01T00:00:00Z"),
        )
    )
    return itsystem.uuid


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
        "LISTEN_TO_CHANGES_IN_MO": "False",
        "CONVERSION_MAPPING": json.dumps(
            {"username_generator": {"existing_usernames_itsystem": "account_name"}}
        ),
    }
)
@pytest.mark.parametrize(
    "at",
    [
        "2026-06-01T00:00:00Z",  # aware: midnight UTC == 02:00 Copenhagen
        "2026-06-01T02:00:00",  # naive: assumed Copenhagen, not midnight
    ],
)
@pytest.mark.usefixtures("ldap_person")
async def test_account_names_at_not_midnight(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    account_name_itsystem: UUID,
    mo_person: UUID,
    at: str,
) -> None:
    await graphql_client.ituser_create(
        ITUserCreateInput(
            user_key="cba",
            itsystem=account_name_itsystem,
            person=mo_person,
            validity=RAValidityInput(from_="1980-01-01T00:00:00"),
        )
    )

    response = await test_client.post(
        "/fixup/delete_non_existing_account_names",
        params={"at": at, "dry_run": "False"},
    )
    assert response.status_code == 500
    assert "AssertionError: 'at' is not at midnight" in response.text


