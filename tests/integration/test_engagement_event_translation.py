@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_MO": "False",
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
    }
)
# TODO: Remove this xfail when MO supports changing the engagement person, or if we
#       decide to disallow setting engagement person via the API(s).
@pytest.mark.xfail(reason="MO does not support changing engagement person")
async def test_engagement2person_change_person(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    integration_graphql_client: GraphQLClient,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
) -> None:
    person1 = await graphql_client.person_create(
        input=EmployeeCreateInput(
            given_name="Aage",
            surname="Aagaard",
            cpr_number="0101010123",
        )
    )

    person2 = await graphql_client.person_create(
        input=EmployeeCreateInput(
            given_name="Betina",
            surname="Bundgaard",
            cpr_number="0101011234",
        )
    )

    engagement = await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key="engagement",
            person=person1.uuid,
            org_unit=mo_org_unit,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            validity={"from": datetime(2000, 1, 1), "to": datetime(2010, 1, 1)},
        )
    )
    await graphql_client.engagement_update(
        input=EngagementUpdateInput(
            uuid=engagement.uuid,
            person=person2.uuid,
            validity={"from": datetime(3000, 1, 1), "to": datetime(3010, 1, 1)},
        )
    )

    integration_graphql_client.person_refresh = AsyncMock()  # type: ignore

    result = await test_client.post(
        "/mo2ldap/engagement",
        headers={"Content-Type": "text/plain"},
        content=str(engagement.uuid),
    )
    result.raise_for_status()

    # Check that we send the event to MO
    args = one(integration_graphql_client.person_refresh.call_args_list).args
    exchange, uuids = args
    assert exchange == "os2mo_ldap_ie"
    assert set(uuids) == {person1.uuid, person2.uuid}
