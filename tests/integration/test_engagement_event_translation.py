from datetime import datetime
from unittest.mock import AsyncMock
from uuid import UUID

import pytest
from httpx import AsyncClient
from more_itertools import one

from mo_ldap_import_export.autogenerated_graphql_client import EngagementCreateInput
from mo_ldap_import_export.autogenerated_graphql_client import GraphQLClient
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    EmployeeCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    EngagementUpdateInput,
)


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_MO": "False",
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
    }
)
@pytest.mark.parametrize(
    "start,end",
    [
        # Current
        (datetime(1970, 1, 1), None),
        (datetime(1970, 1, 1), datetime(3000, 1, 1)),
        # Past
        (datetime(1970, 1, 1), datetime(1980, 1, 1)),
        # Future
        (datetime(2900, 1, 1), datetime(3000, 1, 1)),
    ],
)
async def test_engagement2person(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    integration_graphql_client: GraphQLClient,
    mo_person: UUID,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
    start: datetime | None,
    end: datetime | None,
) -> None:
    engagement = await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key="engagement",
            person=mo_person,
            org_unit=mo_org_unit,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            validity={"from": start, "to": end},
        )
    )

    integration_graphql_client.person_refresh = AsyncMock()  # type: ignore

    result = await test_client.post(
        "/mo2ldap/engagement",
        headers={"Content-Type": "text/plain"},
        content=str(engagement.uuid),
    )
    result.raise_for_status()

    # Check that we send the event to MO
    integration_graphql_client.person_refresh.assert_awaited_once_with(
        "os2mo_ldap_ie", [mo_person]
    )


