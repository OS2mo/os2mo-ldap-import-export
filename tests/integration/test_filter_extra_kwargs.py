import json
from collections.abc import Awaitable
from collections.abc import Callable
from uuid import UUID

import pytest

from mo_ldap_import_export.autogenerated_graphql_client import EmployeeFilter
from mo_ldap_import_export.autogenerated_graphql_client import GraphQLClient
from mo_ldap_import_export.autogenerated_graphql_client import ITUserFilter


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_MO": "False",
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
        "CONVERSION_MAPPING": json.dumps(
            {
                "ldap_to_mo": {
                    "ADtitle": {
                        "objectClass": "ITUser",
                        "_import_to_mo_": "true",
                        "_ldap_attributes_": [],
                        # NOTE: `user_key`is not a valid field on ITUserFilter.
                        # It should have been `user_keys` to work as expected.
                        "uuid": """
                            {{
                                get_ituser_uuid({
                                    'user_key': 'should have been user_keys',
                                    'itsystem': {'user_keys': ['ADtitle']},
                                    'employee': {'uuids': [employee_uuid]},
                                })
                            }}",
                        """,
                        "user_key": "from-ldap",
                        "person": "{{ employee_uuid }}",
                        "itsystem": "{{ get_it_system_uuid('ADtitle') }}",
                    },
                },
            }
        ),
    }
)
@pytest.mark.usefixtures("adtitle")
async def test_get_ituser_uuid_ignores_unknown_kwargs(
    graphql_client: GraphQLClient,
    trigger_ldap_person: Callable[[], Awaitable[None]],
    mo_person: UUID,
) -> None:
    """Prove that misspelled filter kwargs raise a ValidationError."""
    # Prove that no ITUsers exist at the start of the test.
    before = await graphql_client._testing__ituser_read(
        filter=ITUserFilter(employee=EmployeeFilter(uuids=[mo_person])),
    )
    assert before.objects == []

    # Triggering the LDAP -> MO sync fails because of the misspelled filter kwarg
    with pytest.raises(AssertionError) as exc_info:
        await trigger_ldap_person()
    assert "Internal Server Error" in str(exc_info.value)

    # The ITUser was not created because the sync failed.
    after = await graphql_client._testing__ituser_read(
        filter=ITUserFilter(employee=EmployeeFilter(uuids=[mo_person])),
    )
    assert after.objects == []
